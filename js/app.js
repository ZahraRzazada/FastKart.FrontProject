//--------------------basket---------------------------------------
let cards = document.querySelectorAll('#Fruit .slider3 .fruit-cards')
let count = 0;
for(let card of cards) {
    card.setAttribute('id',++count)
}

if(localStorage.getItem('products') === null) {
    localStorage.setItem('products',JSON.stringify([]))
}


let buttons = document.querySelectorAll('#Fruit .slider3 .fruit-cards .fruit-content .fcontent-right button');
console.log(buttons);
for(let btn of buttons) {
    btn.onclick = function(e) {
        e.preventDefault();
        let items = JSON.parse(localStorage.getItem('products'))

        let id = this.parentElement.parentElement.parentElement.id;
        let title = this.parentElement.previousElementSibling.children[5].innerHTML
        let price =this.parentElement.previousElementSibling.children[6].children[0].innerHTML
        let image = this.parentElement.parentElement.previousElementSibling.previousElementSibling.children[0].src    
        let existProd = items.find(x => x.Id === id)
        console.log(existProd);
        if(existProd === undefined) {
            items.push({
                Id: id,
                Title: title,
                Price: price,
                Image: image,
                Count: 1
            })
        }
        else{
            existProd.Count += 1
        }
        localStorage.setItem("products",JSON.stringify(items));
    }
}


function getCount() {
    let items = JSON.parse(localStorage.getItem('products'))
    document.querySelectorAll('header .hb-all .header-bottom nav  .headerfunctions .cartgo #count').innerHTML = items.length
}
getCount()
